import "dotenv/config";
import { readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import OpenAI from "openai";

const root = process.cwd();
const archiveId = "test-archive-001";
const dataPath = path.join(root, "output", "archive-data", `self-reviser-${archiveId}-pipeline.json`);
const outputDir = path.join(root, "output", "paper-variants");
const htmlDir = path.join(root, "tmp", "archive-render");
const data = JSON.parse(await readFile(dataPath, "utf8"));
const manuscript = data.finalManuscript;

if (!process.env.OPENAI_API_KEY || !process.env.OPENAI_MODEL) {
  throw new Error("OPENAI_API_KEY and OPENAI_MODEL are required for this local paper variant.");
}
await Promise.all([mkdir(outputDir, { recursive: true }), mkdir(htmlDir, { recursive: true })]);

const schema = {
  type: "object",
  additionalProperties: false,
  required: ["title", "abstract", "keywords", "paper"],
  properties: {
    title: { type: "string" },
    abstract: { type: "string" },
    keywords: { type: "array", minItems: 4, maxItems: 6, items: { type: "string" } },
    paper: { type: "array", minItems: 4, maxItems: 6, items: { type: "string" } },
  },
};

const client = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
const response = await client.responses.create({
  model: process.env.OPENAI_MODEL,
  input: [
    {
      role: "developer",
      content: [{ type: "input_text", text: `You are preparing a Chinese qualitative social-science institutional narrative report. Produce a visually testable final-paper alternative from the supplied accepted Pass 6 manuscript.

The task is to make the reasoning more theoretically plural and more complex, not to make it more certain. The source manuscript is the only evidence. Do not add events, motives, diagnoses, demographic claims, or factual claims about the author.

Write 4–6 substantial argumentative paragraphs in Chinese. Develop a coherent argument rather than commenting sentence by sentence. Every theoretical reference must be concise, plausible, and directly tied to a feature in the supplied manuscript. Use cautious formulations such as “可能”, “可被理解为”, “并不必然意味着”, “材料不足以证实”.

Do not let Goffman dominate. Use at most one short Goffman reference. Where supported by the text, distribute the analysis across some of these distinct frameworks: Social Comparison Theory (Festinger, 1954) for comparative self-evaluation; Self-Discrepancy Theory (Higgins, 1987) for tension between expressed, perceived, and intended self; Hacking (1999) for the provisional effects of classificatory language on subjectivity; Goffman (1959) for social presentation. Do not include a framework merely because it is listed. Do not create a bibliography.

Return only a title, 120–170 Chinese-character abstract, 4–6 keywords, and the paper paragraphs.` }],
    },
    { role: "user", content: [{ type: "input_text", text: manuscript }] },
  ],
  text: { format: { type: "json_schema", name: "plural_theory_paper", strict: true, schema } },
});

const paper = JSON.parse(response.output_text);
const esc = (value) => String(value ?? "").replaceAll("&", "&amp;").replaceAll("<", "&lt;").replaceAll(">", "&gt;").replaceAll('"', "&quot;");
const bodyGroups = [];
let group = [];
let length = 0;
paper.paper.forEach((paragraph) => {
  if (group.length && length + paragraph.length > 850) { bodyGroups.push(group); group = []; length = 0; }
  group.push(paragraph); length += paragraph.length;
});
if (group.length) bodyGroups.push(group);

const header = `<header class="paper-header"><span>SELF REVISER — CASE 001</span><span>INSTITUTIONAL NARRATIVE REPORT</span></header><div class="paper-rule"></div>`;
const cover = `<section class="sheet cover"><header class="paper-header"><span>SELF REVISER<br>INSTITUTIONAL NARRATIVE REPORT</span><span>CASE NO.<br><b>001B</b></span></header><div class="paper-rule"></div><div class="cover-centre"><h1>${esc(paper.title)}</h1><div class="cover-line"></div><dl><dt>Author</dt><dd>Anonymous Author 001</dd><dt>Generated by</dt><dd>Self Reviser v1.6</dd><dt>Variant</dt><dd>Plural theoretical reading</dd><dt>Date</dt><dd>19 July 2026</dd><dt>Distribution</dt><dd>Internal Use Only</dd></dl></div><footer>This version retains the accepted Pass 6 as its evidentiary base while testing a more plural institutional reading.</footer></section>`;
const abstract = `<section class="sheet abstract-page">${header}<div class="abstract-content"><h2>Abstract</h2><p>${esc(paper.abstract)}</p><div class="keywords"><h3>Keywords</h3><p>${paper.keywords.map(esc).join(" &nbsp; | &nbsp; ")}</p></div></div><footer>ii</footer></section>`;
const body = bodyGroups.map((part, index) => `<section class="sheet body-page">${header}<article class="paper-body">${index === 0 ? "<h2>Institutional Narrative</h2>" : ""}${part.map((paragraph) => `<p>${esc(paragraph)}</p>`).join("")}</article><footer>${index + 1}</footer></section>`).join("\n");
const html = `<!doctype html><html lang="zh"><head><meta charset="utf-8"><title>${esc(paper.title)}</title><style>
@page{size:A4 portrait;margin:0}*{box-sizing:border-box}body{margin:0;background:#fff;color:#111;font-family:"Times New Roman","Songti SC","STSong",serif}.sheet{position:relative;width:210mm;min-height:297mm;padding:18mm 20mm;background:#fff;break-after:page;page-break-after:always}.paper-header{display:flex;justify-content:space-between;gap:12mm;color:#222;font:700 7.5pt/1.45 "Times New Roman","Songti SC",serif;letter-spacing:.45pt}.paper-rule{height:1px;margin:4mm 0 13mm;background:#777}.cover .paper-header{min-height:16mm}.cover-centre{margin-top:46mm;text-align:center}.cover h1{max-width:130mm;margin:0 auto;font-weight:400;font-size:28pt;line-height:1.23}.cover-line{width:35mm;height:1px;margin:14mm auto 20mm;background:#777}.cover dl{display:grid;grid-template-columns:30mm 1px 1fr;width:111mm;margin:0 auto;text-align:left;font-size:10pt;line-height:2}.cover dl::before{content:"";grid-column:2;grid-row:1 / span 5;background:#999}.cover dt{grid-column:1;margin:0}.cover dd{grid-column:3;margin:0;padding-left:6mm}.sheet footer{position:absolute;right:20mm;bottom:13mm;left:20mm;padding-top:6mm;border-top:1px solid #888;color:#222;text-align:center;font-size:7.5pt;line-height:1.55;font-style:italic}.abstract-content{margin-top:23mm}.abstract-content h2{margin:0 0 11mm;font-size:22pt;font-weight:400}.abstract-content>p{max-width:142mm;margin:0;font-size:11pt;line-height:1.9}.keywords{margin-top:27mm;padding-top:8mm;border-top:1px solid #999}.keywords h3{margin:0 0 5mm;font-size:12pt;font-weight:400}.keywords p{font-size:9.5pt;line-height:1.7}.paper-body{font-size:10.8pt;line-height:1.85}.paper-body h2{margin:22mm 0 14mm;font-size:20pt;font-weight:400}.paper-body p{margin:0 0 7.5mm;text-align:left}@media screen{body{padding:8mm 0}.sheet{margin:0 auto 8mm;box-shadow:0 1mm 4mm rgba(0,0,0,.18)}}
</style></head><body>${cover}${abstract}${body}</body></html>`;
const jsonPath = path.join(outputDir, `self-reviser-${archiveId}-plural-theory-paper.json`);
const htmlPath = path.join(htmlDir, `${archiveId}-plural-theory-paper.html`);
await writeFile(jsonPath, JSON.stringify(paper, null, 2), "utf8");
await writeFile(htmlPath, html, "utf8");
console.log(JSON.stringify({ jsonPath, htmlPath, pages: 2 + bodyGroups.length }, null, 2));
