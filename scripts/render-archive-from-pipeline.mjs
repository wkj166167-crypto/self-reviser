import { readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const archiveId = process.argv[2] || "test-archive-001";
const dataPath = path.join(root, "output", "archive-data", `self-reviser-${archiveId}-pipeline.json`);
const htmlPath = path.join(root, "tmp", "archive-render", `${archiveId}.html`);
const data = JSON.parse(await readFile(dataPath, "utf8"));
await mkdir(path.dirname(htmlPath), { recursive: true });

const esc = (value) => String(value ?? "").replaceAll("&", "&amp;").replaceAll("<", "&lt;").replaceAll(">", "&gt;").replaceAll('"', "&quot;");
const comments = data.comments || {};
const numberedComments = new Map();
let commentNumber = 1;
data.sourceParagraphs.forEach((paragraph) => {
  numberedComments.set(paragraph.id, (comments[paragraph.id] || []).map((note) => ({ ...note, globalNumber: commentNumber++ })));
});

function mark(text, notes) {
  let html = esc(text);
  (notes || []).forEach((note) => {
    const quote = esc(note.source_quote || "");
    if (quote && html.includes(quote)) html = html.replace(quote, `<mark>${quote}</mark>`);
  });
  return html;
}

function stagePage(label, paragraphs, { draft = false } = {}) {
  const notes = draft ? paragraphs.flatMap((paragraph) => (numberedComments.get(paragraph.id) || []).map((note) => ({ ...note, paragraph }))) : [];
  const body = paragraphs.map((paragraph) => `<p data-paragraph-id="${esc(paragraph.id)}">${draft ? mark(paragraph.source, comments[paragraph.id]) : paragraph.html}</p>`).join("");
  const notesHtml = notes.map((note) => `<div class="comment-card"><span class="note-number">${note.globalNumber}</span>${esc(note.text)}</div>`).join("");
  return `<section class="sheet word-sheet">
    <header class="word-header"><span></span><span>${label}</span></header>
    <div class="word-rule"></div>
    <div class="word-stage ${draft ? "with-comments" : ""}">
      <article class="word-text">${body}</article>${draft ? `<aside class="word-comments">${notesHtml}</aside>` : ""}
    </div>
  </section>`;
}

function groupsForStage(stage, pass) {
  const groups = [];
  let current = [];
  let weight = 0;
  const limit = pass <= 1 ? 1300 : pass <= 3 ? 1120 : pass <= 4 ? 900 : 720;
  stage.forEach((paragraph) => {
    const plain = String(paragraph.text || "").length;
    const markup = Math.max(0, String(paragraph.html || "").length - plain) * 0.24;
    const cost = plain + markup;
    if (current.length && weight + cost > limit) {
      groups.push(current);
      current = [];
      weight = 0;
    }
    current.push(paragraph);
    weight += cost;
  });
  if (current.length) groups.push(current);
  return groups;
}

function coverPage() {
  return `<section class="sheet paper cover"><header class="paper-header"><span>SELF REVISER<br>INSTITUTIONAL NARRATIVE REPORT</span><span>CASE NO.<br><b>001</b></span></header><div class="paper-rule"></div><div class="cover-centre"><h1>${esc(data.metadata.title)}</h1><div class="cover-line"></div><dl><dt>Author</dt><dd>Anonymous Author 001</dd><dt>Generated by</dt><dd>Self Reviser v1.6</dd><dt>Date</dt><dd>19 July 2026</dd><dt>Classification</dt><dd>Interpretive Report</dd><dt>Distribution</dt><dd>Internal Use Only</dd></dl></div><footer>This report is generated through iterative editorial interpretation.<br>It does not represent the author’s original words, but an institutional reading of the submitted narrative.</footer></section>`;
}

function abstractPage() {
  return `<section class="sheet paper abstract-page"><header class="paper-header"><span>SELF REVISER — CASE 001</span><span>INSTITUTIONAL NARRATIVE REPORT</span></header><div class="paper-rule"></div><div class="abstract-content"><h2>Abstract</h2><p>${esc(data.metadata.abstract)}</p><div class="keywords"><h3>Keywords</h3><p>${data.metadata.keywords.map(esc).join(" &nbsp; | &nbsp; ")}</p></div></div><footer>ii</footer></section>`;
}

function bodyPage(paragraphs, index, continued = false) {
  return `<section class="sheet paper body-page"><header class="paper-header"><span>SELF REVISER — CASE 001</span><span>INSTITUTIONAL NARRATIVE REPORT</span></header><div class="paper-rule"></div><article class="paper-body">${continued ? "" : "<h2>Institutional Narrative</h2>"}${paragraphs.map((paragraph) => `<p>${esc(paragraph)}</p>`).join("")}</article><footer>${index}</footer></section>`;
}

