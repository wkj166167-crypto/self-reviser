// A submitted archive is one manuscript: all source paragraphs and all margin
// notes remain in one continuously numbered Draft document until a real A4
// overflow makes another page necessary.
const draft = data.sourceParagraphs.map((paragraph) => ({ ...paragraph, text: paragraph.source }));
if (draft.length) sheets.push(stagePage("DRAFT + EDITORIAL NOTES", draft, { draft: true }));

for (const pass of [1, 2, 3, 4, 5, 6]) {
  const stage = data.stages[String(pass)] || [];
  if (stage.length) sheets.push(stagePage(`PASS ${pass}`, stage));
}
sheets.push(coverPage(), abstractPage());
const finalParagraphs = String(data.finalManuscript || "").split(/\n\s*\n+/).filter(Boolean);
const finalGroups = [];
let group = [];
let weight = 0;
finalParagraphs.forEach((paragraph) => {
  // The accepted Pass 6 paragraphs are argumentative and substantially longer
  // than the original Draft. Keep paper pages conservative so headers and
  // footers are never separated from their text by browser pagination.
  if (group.length && weight + paragraph.length > 820) { finalGroups.push(group); group = []; weight = 0; }
  group.push(paragraph); weight += paragraph.length;
});
if (group.length) finalGroups.push(group);
finalGroups.forEach((part, index) => sheets.push(bodyPage(part, index + 1, index > 0)));

